package main


func main(){
	println(
		"true  && true ->",
		true && true,
	)
	println(
		"true  && false ->",
		true && false,
	)
	println(
		"false && true ->",
		false && true,
	)
	println(
		"false && false ->",
		false && false,
		)
	
	println(
		"true  || true ->",
		true || true,
		)
	println(
		"true  || false ->",
		)
	println(
		"false || true ->",
		false || true,
		)
	println(
		"false || false ->",
		false || false,
		)
	println(
		"!true ->",
		!true,
	)
	println(
		"!false ->",
		!false,
	)
}